// Define o ambiente de processo: 'producao' ou 'desenvolvimento'
var ambiente_processo = 'desenvolvimento'; // Mude para 'producao' quando necessário

// Define o caminho do arquivo .env com base no ambiente
var caminho_env = ambiente_processo === 'producao' ? '.env' : '.env.dev';
// A sintaxe do operador ternário é: condição ? valor_se_verdadeiro : valor_se_falso

require("dotenv").config({ path: caminho_env });

var express = require("express");
var cors = require("cors");
var path = require("path");
var PORTA_APP = process.env.APP_PORT;
var HOST_APP = process.env.APP_HOST;

var app = express();

var indexRouter = require("./src/routes/index");
var usuariosRouter = require("./src/routes/usuarios");
var sensoresRouter = require("./src/routes/sensores");
var alertasRouter = require("./src/routes/alertas");
var cozinhasRouter = require("./src/routes/cozinhas");
var relatóriosRouter = require("./src/routes/relatorios");
var empresasRouter = require("./src/routes/empresas");

app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(express.static(path.join(__dirname, "public")));

app.use(cors());

app.use("/", indexRouter);
app.use("/usuarios", usuariosRouter);
app.use("/sensores", sensoresRouter);
app.use("/alertas", alertasRouter);
app.use("/cozinhas", cozinhasRouter);
app.use("/relatorios", relatóriosRouter);
app.use("/empresas", empresasRouter);

